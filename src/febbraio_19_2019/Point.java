package febbraio_19_2019;

/*
 * Classe Punto, con coordinate x e y.
*/
public class Point{
	double x;
	double y;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}	
}
